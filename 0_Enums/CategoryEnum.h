#ifndef CATEGORYENUM_H
#define CATEGORYENUM_H

/**
 * @enum CategoryEnum
 * @brief Represents the category of a word.
 * 
 * Used in the Word class to classify words into different categories.
 */
enum class CategoryEnum {

    /** Animals category */
    Animals,  

    /** Plants category */
    Plants,  

    /** Technology category */
    Technology,  

    /** Cities category */
    Cities,  

    /** Jobs category */
    Jobs,  

    /** Movies category */
    Movies,  

    /** Category unspecified or unknown */
    Unspecified
};

#endif // CATEGORYENUM_H
